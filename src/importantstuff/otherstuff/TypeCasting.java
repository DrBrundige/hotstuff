package importantstuff.otherstuff;

import java.util.Scanner;

public class TypeCasting {
	public static void main (String [] args) {
		char letterToQuit = '?';
		int  numPresses   = 0;

		letterToQuit = 'q';
		numPresses = 2;

		System.out.println("Press the " + letterToQuit + " key " + numPresses + " times to quit.");

		return;
	}
}